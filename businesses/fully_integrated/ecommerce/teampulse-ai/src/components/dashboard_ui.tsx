import React, { FC, ReactNode, Key } from 'react';

interface Props {
  message: string;
  children?: ReactNode;
  className?: string;
  dataTestId?: string;
}

const DashboardUI: FC<Props> = ({ message, children, className, dataTestId }) => {
  const uniqueKey = useUniqueKey();

  return (
    <div data-testid={dataTestId} className={`dashboard-message ${className || ''}`} key={uniqueKey}>
      {message}
      {children}
    </div>
  );
};

const useUniqueKey = () => {
  const [key, setKey] = React.useState<Key>(0);

  React.useEffect(() => {
    setKey(prevKey => prevKey + 1);
  }, []);

  return key;
};

// Import necessary styles for better UI consistency
import './DashboardUI.scss';

export default DashboardUI;

